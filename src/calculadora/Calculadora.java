package calculadora;

public class Calculadora {

    double soma(double n1, double  n2){
        return n1+n2;
    }
    double sub(double n1, double n2){
        return n1-n2;
    }
    double mult(double n1,double n2){
        return n1*n2;
    }
    double div(double n1, double n2){
        return n1/n2;
    }
}
